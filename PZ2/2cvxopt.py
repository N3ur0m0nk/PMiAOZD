import time
import cvxopt.modeling
import matplotlib.pyplot as plt

start = time.time()
x = cvxopt.modeling.variable(2, 'x')
z = -(30 * x[0] + 1 * x[1])
mass1 = (90 * x[0] + 5 * x[1] <= 10000)
mass2 = (3 * x[0] - x[1] == 0)
x_non_negative = (x >= 0)
problem = cvxopt.modeling.op(z, [mass1, mass2, x_non_negative])
problem.solve(solver='glpk')
print("Прибыль:")
print(abs(problem.objective.value()[0]))
print("Результат:")
print(x.value)
stop = time.time()
print("Время :")
print(stop - start)

plt.title('ПЗ 2')
plt.xlabel('Значение X1')
plt.ylabel('Значение X2')
plt.xlim(0, 150)
plt.ylim(0, 300)
plt.axline((0, 2000), slope=-18)
plt.axline((0, 0), slope=3)
plt.fill_between((0, 2000/21, 111.111), (0, 2000/7, 0))
plt.text(4, 25, 'x2=3*x1', rotation=50)
plt.text(97, 180, 'x2<=2000-18*x1', rotation=-80)
plt.grid()
plt.show()